-- تعيين الموظفين لورشة التركيب (الورشة موجودة مسبقاً)
UPDATE employees e
JOIN workshops w ON w.name = 'التركيب'
SET e.workshop_id = w.id
WHERE e.employee_code IN ('25','27','46','64','79','81','110','121','122','124',
                           '126','130','132','138','169','171','182','184','188',
                           '190','198','247','261','308','312','361','384','399',
                           '403','405','427','445');

-- تحقق من النتيجة
SELECT e.employee_code, e.name, w.name AS workshop
FROM employees e
LEFT JOIN workshops w ON w.id = e.workshop_id
WHERE e.employee_code IN ('25','27','46','64','79','81','110','121','122','124',
                           '126','130','132','138','169','171','182','184','188',
                           '190','198','247','261','308','312','361','384','399',
                           '403','405','427','445')
ORDER BY CAST(e.employee_code AS UNSIGNED);
